<?php
require_once '../apps/'.$_PETICION->modulo.'/modelos/EstadosOrdenCompra_modelo.php';
class estados_de_orden_de_compra extends Controlador{
	var $modelo="EstadosOrdenCompra";
	var $campos=array('id','nombre');
	
	function nuevo(){		
		$campos=$this->campos;
		$vista=$this->getVista();				
		for($i=0; $i<sizeof($campos); $i++){
			$obj[$campos[$i]]='';
		}
		$vista->datos=$obj;		
		
		global $_PETICION;
		$vista->mostrar('/'.$_PETICION->controlador.'/edicion');
		
		
	}
	
	function guardar(){
		return parent::guardar();
	}
	function borrar(){
		return parent::borrar();
	}
	function editar(){
		return parent::editar();
	}
	function buscar(){
		return parent::buscar();
	}
}
?>